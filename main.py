CONFIG_PATH = Path(os.environ.get("HDY_CONFIG", str(Path(__file__).parent / "config.json")))

def _load_runtime_cfg() -> dict[str, Any]:
    if CONFIG_PATH.exists():
        with open(CONFIG_PATH) as f:
            return json.load(f)
    # Fallback defaults for development
    return {
        "admin_username": "admin",
        "admin_password_hash": CryptContext(schemes=["bcrypt"]).hash("admin"),
        "secret_key": "dev-secret-key-change-me",
        "port": 8080,
        "base_path": "",
    }


_runtime = _load_runtime_cfg()
_pwd_ctx = CryptContext(schemes=["bcrypt"], deprecated="auto")
_SECRET_KEY: str = _runtime.get("secret_key", "changeme")
_ALGORITHM = "HS256"
_TOKEN_EXPIRE_HOURS = 24

