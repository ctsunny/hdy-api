_ASSETS_DIR = _STATIC_DIR / "assets"

if _STATIC_DIR.exists():
    # Serve static assets (only mount if the assets subdirectory exists)
    if _ASSETS_DIR.exists():
        app.mount(f"{BASE_PATH}/assets", StaticFiles(directory=str(_ASSETS_DIR)), name="assets")

    @app.get(f"{BASE_PATH}/", include_in_schema=False)
    @app.get(f"{BASE_PATH}", include_in_schema=False)
    async def serve_index() -> FileResponse:
        return FileResponse(str(_STATIC_DIR / "index.html"))

    # Catch-all for SPA routing
    @app.get(f"{BASE_PATH}/{{full_path:path}}", include_in_schema=False)
    async def spa_fallback(full_path: str) -> FileResponse:
        if full_path.startswith("api/"):
            raise HTTPException(status_code=404)
        return FileResponse(str(_STATIC_DIR / "index.html"))
